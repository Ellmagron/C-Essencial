/*
    Generics e Coleções Genéricas
    - Generics permitem criar classes, métodos, interfaces e delegados que não tem um tipo pré definido, o ou seja o tipo é definido no momento da instanciação pelo usuário.
    
    sintaxe: generic <T> onde T é o tipo genérico  e <> é o Parênteses angular , ou seja, o tipo que será definido no momento da instanciação.

    public class MinhaClasse<T>
    {   
    }

    É possível usar mais de um parametro de tipo genérico, por exemplo: 
    public class MinhaClasse<T, U>
    {   
    }

    -Generics- restrições Constraints
 */