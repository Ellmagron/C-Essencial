/*Herança:
    Como ler um relacionamento de herança  entre uma classe base e uma classe derivada em C#?
        -Podemos ler a herança(:) como "É um(a)".
        -Exemplo: ContaInvestimento É uma Conta.
A Herança é usada para expressar um relacionamento "É um(a)" entre uma classe base e uma ou mais classes derivadas, onde as classes
derivadas são versões especializadas da classe base.

A partir do visual studio 2022, é possivel utilizar o componente o digrama de classe, que permite representar as classes e o relacionamentro entre elas

C# possui os seguintes tipos de heranças
    - Simples
    -Hierárquica
    -Multinivel
    -Múltipla

- Simples: É quando herança ocorre apenas entre uma classe base e uma derivada
- Hierárquica: É quando uma varias classes derivadas herdam de uma mesma classe base.
- Multinível: É quando uma classe deriva de outra classe derivada
- Múltipla : Ocorre quando temos uma classe base e uma classe derivada que implementa uma ou mais interfaces

 */